#include "libft.h"

/**
 * @brief Calcule le carré d'un entier.
 *
 * Cette fonction prend un entier en argument et retourne son carré.
 *
 * @param i L'entier à mettre au carré.
 * @return Le carré de l'entier.
 */
int	rc(int i)
{
	return (i * i);
}
